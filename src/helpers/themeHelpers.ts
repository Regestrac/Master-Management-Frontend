export const setPrimaryPalette = (color: string) => {
  const palette = colorPalettes[color];
  if (palette) {
    Object.entries(palette).forEach(([key, value]) => {
      document.documentElement.style.setProperty(`--color-primary-${key}`, value);
    });
  }
};
